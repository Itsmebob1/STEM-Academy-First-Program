days = ["monday", "tuesday", "wednesday", "thursday", "friday", "saturday", "sunday"]
for day in days:
  print(day)
  if day == 'monday':
      print("1")
  if day == 'tuesday':
      print("2")
  if day == 'wednesday':
      print("3")
  if day == 'thursday':
      print("4")
  if day == 'friday':
      print("5")
  if day == 'saturday':
      print("6")
  if day == 'sunday':
      print("7")
